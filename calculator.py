print("Calculator")
num_1=float(input("Enter First Number:"))
op_type=input("Enter Operation:")
num_2=float(input("Enter Second Number:"))
if op_type == "+":
    print(num_1+num_2)
if op_type == "-":
    print(num_1-num_2)
if op_type == "*":
    print(num_1*num_2)
if op_type == "/":
    print(num_1/num_2)
